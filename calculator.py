class Calculator():

    def Sum(self, user_first_number, user_second_number):
        return user_first_number + user_second_number

    def Del(self, user_first_number, user_second_number):
        return user_first_number - user_second_number

    def Multiple(self, user_first_number, user_second_number):
        return user_first_number * user_second_number

    def Div(self, user_first_number, user_second_number):
        return user_first_number / user_second_number




